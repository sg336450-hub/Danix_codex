###   Operadores ###

# Aritméticos   
print(2 + 3) # Suma
print(5 - 2) # Resta
print(4 * 3) # Multiplicación
print(10 / 2) # División
print(10 // 3) # División entera
print(10 % 3) # Módulo
print(2 ** 3) # Potenciación

# Comparación

print(5 > 3) # Mayor que
print(5 < 3) # Menor que
print(5 == 5) # Igual a
print(5 != 3) # Distinto de
print(5 >= 5) # Mayor o igual que
print(5 <= 3) # Menor o igual que

# Lógicos

print(True and False) # AND lógico
print(True or False) # OR lógico
print(not True) # NOT lógico


